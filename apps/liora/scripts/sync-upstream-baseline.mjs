#!/usr/bin/env node
import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { dirname, join, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(here, '..', '..', '..');
const lockPath = join(repoRoot, 'meta', 'upstream.lock.yaml');
const outPath = join(here, '..', 'src', 'generated', 'upstream-baseline.generated.ts');

const REQUIRED_FIELDS = [
  'product',
  'repository',
  'version',
  'ref',
  'syncedAt',
  'superlioraCommit',
];

function parseSimpleYaml(text) {
  const values = {};
  for (const line of text.split('\n')) {
    const trimmed = line.trim();
    if (trimmed.length === 0 || trimmed.startsWith('#')) continue;
    const separator = trimmed.indexOf(':');
    if (separator === -1) {
      throw new Error(`Invalid upstream lock line: ${line}`);
    }
    const key = trimmed.slice(0, separator).trim();
    let value = trimmed.slice(separator + 1).trim();
    if (
      (value.startsWith('"') && value.endsWith('"')) ||
      (value.startsWith("'") && value.endsWith("'"))
    ) {
      value = value.slice(1, -1);
    }
    values[key] = value;
  }
  return values;
}

const lockText = readFileSync(lockPath, 'utf8');
const parsed = parseSimpleYaml(lockText);

for (const field of REQUIRED_FIELDS) {
  if (typeof parsed[field] !== 'string' || parsed[field].length === 0) {
    throw new Error(`meta/upstream.lock.yaml is missing required field "${field}"`);
  }
}

const baseline = {
  product: parsed.product,
  repository: parsed.repository,
  version: parsed.version,
  ref: parsed.ref,
  syncedAt: parsed.syncedAt,
  superlioraCommit: parsed.superlioraCommit,
  notes: typeof parsed.notes === 'string' && parsed.notes.length > 0 ? parsed.notes : undefined,
};

mkdirSync(dirname(outPath), { recursive: true });
writeFileSync(
  outPath,
  [
    '// GENERATED by scripts/sync-upstream-baseline.mjs — do not edit.',
    `export const UPSTREAM_BASELINE = ${JSON.stringify(baseline, null, 2)} as const;`,
    '',
  ].join('\n'),
);

console.log(`[sync-upstream-baseline] wrote ${outPath}`);
