#!/usr/bin/env node
/**
 * Build script: aggregates expert personas from multiple open-source catalogs
 * and generates packages/agent-core/src/expert-agents/catalog.ts
 *
 * Runtime composition: packages/agent-core/src/expert-agents/expert-persona.ts
 *
 * Sources:
 * - github.com/msitarzewski/agency-agents
 * - github.com/jee599/agentcrow
 * - github.com/VoltAgent/awesome-claude-code-subagents
 * - github.com/EricGrill/agent-personalities-skills
 */
import { writeFile, mkdir } from 'node:fs/promises';
import { createRequire } from 'node:module';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';
import {
  DIVISION_META,
  fetchAllExpertSources,
} from './lib/expert-catalog-sources.mjs';

const repoRoot = join(dirname(fileURLToPath(import.meta.url)), '..');
const requireFromAgentCore = createRequire(join(repoRoot, 'packages/agent-core/package.json'));
const skipEmbeddings = process.argv.includes('--skip-embeddings');

async function generateEmbeddings(experts) {
  let pipeline;
  try {
    ({ pipeline } = await import(requireFromAgentCore.resolve('@huggingface/transformers')));
  } catch (error) {
    console.warn(
      'Skipping embeddings: install @huggingface/transformers to enable semantic search vectors.',
    );
    console.warn(String(error instanceof Error ? error.message : error));
    return;
  }

  console.log('Generating embeddings...');
  const extractor = await pipeline(
    'feature-extraction',
    'ibm-granite/granite-embedding-97m-multilingual-r2',
    { dtype: 'fp32' },
  );

  for (const expert of experts) {
    const text = [
      expert.name,
      expert.description,
      expert.vibe,
      `Division: ${expert.division}`,
      `Tags: ${expert.tags.join(', ')}`,
      `Capabilities: ${expert.capabilities.join(', ')}`,
    ].join('. ');
    const output = await extractor(text, { pooling: 'mean', normalize: true });
    expert.embedding = Array.from(output.data ?? output[0]?.data ?? []);
  }
  console.log('Embeddings generated.');
}

async function buildCatalog() {
  const { experts, counts } = await fetchAllExpertSources();
  console.log('Source counts:', counts);

  if (!skipEmbeddings) {
    await generateEmbeddings(experts);
  } else {
    console.log('Skipping embeddings (--skip-embeddings).');
  }

  const metaExperts = experts.map((expert) => {
    const rest = { ...expert };
    delete rest.personaText;
    return { ...rest, personaText: '' };
  });
  const personas = Object.fromEntries(experts.map((e) => [e.id, e.personaText ?? '']));

  const header = `// AUTO-GENERATED by scripts/build-expert-catalog.mjs
// Do not edit manually.
//
// Sources (${counts.total} experts):
// - agency-agents (${counts.agency}): github.com/msitarzewski/agency-agents
// - agentcrow (${counts.agentcrow}): github.com/jee599/agentcrow
// - awesome-claude-code-subagents (${counts.voltagent}): github.com/VoltAgent/awesome-claude-code-subagents
// - agent-personalities-skills (${counts.ericgrill}): github.com/EricGrill/agent-personalities-skills
import type { ExpertCatalogEntry } from './types';
`;

  const metaTs = `${header}
export const EXPERT_CATALOG_META: readonly ExpertCatalogEntry[] = ${JSON.stringify(metaExperts, null, 2)} as const;

export const EXPERT_CATALOG_META_BY_ID: Readonly<Record<string, ExpertCatalogEntry>> = Object.fromEntries(
  EXPERT_CATALOG_META.map((e) => [e.id, e]),
);

export const EXPERT_DIVISIONS = ${JSON.stringify(DIVISION_META, null, 2)} as const;

export const EXPERT_CATALOG_SOURCE_COUNTS = ${JSON.stringify(counts, null, 2)} as const;
`;

  // The catalog.ts compatibility facade is hand-maintained and committed: it
  // only re-exports catalog-meta and defines the persona helpers (including
  // fallbackExpertPersonaText), carrying no per-expert data. Regenerate only
  // the data modules here and never overwrite the facade, or that fallback
  // helper would be silently deleted on the next catalog build.
  const outDir = join(repoRoot, 'packages', 'agent-core', 'src', 'expert-agents');
  await mkdir(outDir, { recursive: true });
  await writeFile(join(outDir, 'catalog-meta.ts'), metaTs, 'utf-8');
  await writeFile(join(outDir, 'catalog-personas.json'), JSON.stringify(personas), 'utf-8');
  console.log(`Wrote catalog-meta.ts (${metaTs.length} bytes), catalog-personas.json (${JSON.stringify(personas).length} bytes)`);
}

buildCatalog().catch((error) => {
  console.error(error);
  process.exit(1);
});
